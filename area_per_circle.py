# This program calculates and displays the area and perimeter of a
# circle with radius 30 mm.
import math


def main():
    print("For a circle that has a radius")
    print("of 30 mm:")
    print("")
    print("Area = {} mm^2". format(math.pi*30**2))
    print("Perimeter = {} mm". format(2*math.pi*30))


if __name__ == "__main__":
    main()
